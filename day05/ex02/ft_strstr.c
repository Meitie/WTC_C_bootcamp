#include <stdio.h>

char *ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	if (!to_find)
		return (str);
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == to_find[0])
		{
			j = 1;
			while (to_find[j] != '\0' && str[i + j] == to_find[j])
				j++;
			if (to_find[j] == '\0')
				return (&str[i]);
		}
		i++;
	}
	return (0);
}

int		main()
{
	printf("%s", ft_strstr("hello there world", "there"));
}
