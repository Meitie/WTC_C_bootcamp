int		ft_atoi(const char *str)
{
	int i = 0;
	int base = 0;
	int neg = 1;

	while ((str[i] == 32) || (str[i] > 8 && str[i] < 17))
		i++;
	if (str[i] == '-' || str[i] == '+')
    {
        if (str[i] == '-')
            neg = -1;
        i++;
    }
	while (str[i] >= '0' && str[i] <= '9')	{
		base = (base * 10) + (str[i] - '0');
		i++;
	}
	return (base * neg);
}
